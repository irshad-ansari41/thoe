                        <p>Azizi Developments has picked up the pace of construction across its various projects in Al Furjan.</p>
                        <p>Azizi Developments currently holds one of the highest concentrations of residential units in Al Furjan, with a total of 12 projects delivered to date. Later this year, new projects will be added to the impressive portfolio – Shaista Azizi, Samia Azizi, Farishta Azizi, Azizi Star, and Azizi Plaza – which together comprise a total of 1,744 units. Their completion is due by the second half of 2018, raising the number of completed projects in Al Furjan to 17 and total units completed to 3,639.</p>
                        <p>Al Furjan is rapidly becoming a sought-after location in Dubai with many restaurants, entertainment options and 5-star hotels in the vicinity. Only a 10-minute drive from Ibn Battuta Mall and adjacent to Discovery Gardens, Jebel Ali Village, and The Gardens, Al Furjan is at the heart of a vibrant community. The new metro line named Route 2020 will connect Al Furjan to locations such as Dubai Investment Park, Discovery Gardens, and The Gardens, and will also eventually service the Expo 2020 site and the Al Maktoum International Airport.</p>
                        <p>The community’s features include retail centres such as Al Furjan Pavilion and Al Furjan West Pavilion, landscaped gardens, and easy access to public transportation. Located between Sheikh Zayed Road and Mohammed bin Rashid Road, Al Furjan also provides residents with easy access to Dubai’s attractions and business districts, and to Abu Dhabi. </p>



