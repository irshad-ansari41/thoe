                        <p><strong>Thoe Developments reaches 20% construction milestone at Thoe Riviera</strong></p>

                        <p>Thoe Developments has announced a construction milestone for its flagship waterfront community project in Meydan One, Thoe Riviera.</p>
                        <p>Comprising of 16,000 units, the AED 12 billion project has crossed the 20 percent completion status. Ground floor works are completed across the mid-rise buildings that comprise of seven to ten floors. With first floor construction ongoing, and second and third floors expected to commence construction in August, Thoe Riviera is progressing towards timely completion.</p>
                        <p>Thoe Riviera, launched in July 2017, is the flagship community project of Thoe Developments. The architecture, while drawing inspiration from the French Riviera lifestyle, will also contain versatile, contemporary elements. Outdoor recreation and proximity to the business hub of Dubai and other leading attractions, including the future Meydan One Mall and the Meydan Racecourse, home of the Dubai World Cup - the world’s richest horse day, further contribute to making Thoe Riviera a sound return on investment.</p>
                        <p>Mirwais Thoe, Chairman of Thoe Group, said: “Thoe Riviera continues to be one of the most unique waterfront residential developments in Dubai. It stands to offer residents and investors high ROI via an appreciation in property value in the buildup to Expo 2020 and beyond. It features a stunning French-Mediterranean approach to design, plenty of amenities, a community-oriented lifestyle and views of the Dubai Canal and Dubai skyline.”</p>

